SELECT n, 'g', 'X', 'a1'
FROM (
    SELECT 2600 + ones.i + (tens.i * 10) + (hundreds.i * 100) AS n
    FROM
      (SELECT 0 AS i UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4 UNION ALL 
       SELECT 5 UNION ALL SELECT 6 UNION ALL SELECT 7 UNION ALL SELECT 8 UNION ALL SELECT 9) AS ones
      CROSS JOIN
      (SELECT 0 AS i UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4 UNION ALL 
       SELECT 5 UNION ALL SELECT 6 UNION ALL SELECT 7 UNION ALL SELECT 8 UNION ALL SELECT 9) AS tens
      CROSS JOIN
      (SELECT 0 AS i UNION ALL SELECT 1 UNION ALL SELECT 2 UNION ALL SELECT 3 UNION ALL SELECT 4 UNION ALL 
       SELECT 5 UNION ALL SELECT 6) AS hundreds
) AS seq
WHERE n BETWEEN 2600 AND 3200
  AND NOT EXISTS (SELECT 1 FROM Position p WHERE p.id = n);

