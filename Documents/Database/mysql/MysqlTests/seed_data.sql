-- We create placeholder rows for a safe numeric range that covers all referenced ids.
-- Note: Values for color/piece/cordinate are dummy but valid for tests that only require referential integrity.
-- Use a numbers generator without CTEs for compatibility with older MySQL/MariaDB.
-- This produces integers 2600..3200 and inserts any missing ids.
